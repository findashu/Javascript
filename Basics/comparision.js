// Comparision Operator
// equality == 
// not equal !=
// greater than >
// greater than equal >=
// less than equal <=
//strict equality ===
// strict not equal !==

var x = 10 , y = 20;
console.log(x == y);

// not equal
console.log(x != y);

//strict equality
var a = 10;
var b = '10';
console.log(a === b);

// strictly not equal
console.log(a !== b);
